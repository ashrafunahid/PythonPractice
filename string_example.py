a = ' Data Science'

# Printing string variable
print(a)

# Printing certain amount of string
print(a[2:5])
print(a[-6:-5])

# Printing length of string
print(len(a))

# Removing space if it is first character
print(a.strip())

# Making all upper case
print(a.upper())

# Making all lower case
print(a.lower())

# Find splited word
print(a.split())

# Replace any word in string variable
print(a.replace(' Data', 'Python'))