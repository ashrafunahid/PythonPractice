import math
# import math as m # Angular Conversion
# help(math)
x = 2
# Square Function
print(math.pow(x, 2))
# Square Root
print(math.sqrt(x))
# Value of Pi
print(math.pi)
# Value of e
print(math.e)
# Factorial Value
print(math.factorial(x))
# Remainder value
print(math.remainder(x, 3))
# Value of tau
print(math.tau)
# Value of inf
print(math.inf)
# Value of nan
print(math.nan)
# Convert radious to degrees
print(math.degrees(x))
# Convert degrees to radious
print(math.radians(x))